a = (2, 5, 4)
b = (5, 8, 1, 2)
c = a + b
print(c)

print(len(c))
# LEN = Mostra quantos valores existem na Tupla

print(c.count(5))
# COUNT = Mostra quantas vezes o valor dentro de parênteses aparece na Tupla

print(c.index(8))
print(c.index(2, 4))
# INDEX = Mostra em qual posição está o valor dentro de parênteses na Tupla
# O Index mostra a primeira ocorrência do valor, no caso de "EX: print(c.index(2, 4)", o segundo número mostra 
# a partir de qual posição na Tupla o Index deve procurar o valor