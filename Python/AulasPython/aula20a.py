def titulo(msg):
    print('-'*30)
    print(f'{msg:^30}')
    print('-'*30)

# def = comando para definir o valor de uma função
# O valor entre parênteses depois do nome da função serve para indicar a existência de uma váriavel dentro da função
# Você pode pedir para o usuário dar um valor para a váriavel ou você mesmo pode colocar no parênteses depois da chamada

titulo('Bom dia')